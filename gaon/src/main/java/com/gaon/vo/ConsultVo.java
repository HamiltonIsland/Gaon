package com.gaon.vo;

public class ConsultVo {
	
	private int consultNo;
	private int teacherNo;
	private int studentNo;
	private String consultDate;
	private String content;
	private String title;
	private String consultDetail;
	private int courseNo;
	
	public int getCourseNo() {
		return courseNo;
	}
	public void setCourseNo(int courseNo) {
		this.courseNo = courseNo;
	}
	public int getConsultNo() {
		return consultNo;
	}
	public void setConsultNo(int consultNo) {
		this.consultNo = consultNo;
	}
	public int getTeacherNo() {
		return teacherNo;
	}
	public void setTeacherNo(int teacherNo) {
		this.teacherNo = teacherNo;
	}
	public int getStudentNo() {
		return studentNo;
	}
	public void setStudentNo(int studentNo) {
		this.studentNo = studentNo;
	}
	public String getConsultDate() {
		return consultDate;
	}
	public void setConsultDate(String consultDate) {
		this.consultDate = consultDate;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getConsultDetail() {
		return consultDetail;
	}
	public void setConsultDetail(String consultDetail) {
		this.consultDetail = consultDetail;
	}
	
	

}
